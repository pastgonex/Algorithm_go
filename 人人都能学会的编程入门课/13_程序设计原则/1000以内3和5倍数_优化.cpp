#include <iostream>

using namespace std;

// 加入数学思维
/*1. 要算1000以内所有的3或者5的倍数之和
  2. 第一个想法是逐个判断
  3. 算出所有3的倍数之和, 和所有5的倍数之和, 但是3和5都会算15的倍数之和, 所以最后再减去一个15的倍数之和
*/

int main(void)
{
    int sum3 = (3 + 999 / 3 * 3) * (999 / 3) / 2; //等差数列
    int sum5 = (5 + 999 / 5 * 5) * (999 / 5) / 2;
    int sum15 = (15 + 999 / 15 * 15) * (999 / 15) / 2;
    printf("%d\n", sum3 + sum5 - sum15);
    return 0;
}
